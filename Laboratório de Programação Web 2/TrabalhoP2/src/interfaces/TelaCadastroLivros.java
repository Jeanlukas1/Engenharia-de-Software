    private void BotaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSalvarActionPerformed
        // TODO add your handling code here:
        // 1. Coletar os dados dos campos de texto (removendo espaços em branco extras).
        String titulo = CampoTitulo.getText().trim();
        String autor = CampoAutor.getText().trim();
        String anoStr = CampoAno.getText().trim();
       
        if (titulo.isEmpty() || autor.isEmpty() || anoStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return; // Interrompe a execução do método se a validação falhar.
        }
        try {           
            int ano = Integer.parseInt(anoStr);          
            if (livroEmEdicao == null) {
                // --- CADASTRAR UM LIVRO NOVO ---
                System.out.println("Modo de Criação: Salvando novo livro.");
                Livro novoLivro = new Livro(titulo, autor, ano);
                listaLivros.add(novoLivro);
                JOptionPane.showMessageDialog(this, "Livro salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            } else {
                // --- ATUALIZAR UM LIVRO EXISTENTE ---
                System.out.println("Modo de Edição: Atualizando livro ID " + livroEmEdicao.getId());
                livroEmEdicao.setTitulo(titulo);
                livroEmEdicao.setAutor(autor);
                livroEmEdicao.setAno(ano);
                JOptionPane.showMessageDialog(this, "Livro atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            }
            limparCampos();
            habilitarCampos(false);
            carregarTabela();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O ano de publicação deve ser um número válido.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
    }
   
    }//GEN-LAST:event_BotaoSalvarActionPerformed

