    private void tabelaLivrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaLivrosMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tabelaLivros.getSelectedRow();

    if (linhaSelecionada != -1) {
        int idLivro = (int) tabelaLivros.getValueAt(linhaSelecionada, 0);

        
        for (Livro livro : listaLivros) {
            if (livro.getId() == idLivro) {
                this.livroEmEdicao = livro; // Armazena o livro para edição
                break;
            }
        }

  
        if (this.livroEmEdicao != null) {
            CampoTitulo.setText(this.livroEmEdicao.getTitulo());
            CampoAutor.setText(this.livroEmEdicao.getAutor());
            CampoAno.setText(String.valueOf(this.livroEmEdicao.getAno()));
            habilitarCampos(true); 
        }
    }
    }//GEN-LAST:event_tabelaLivrosMouseClicked

    private void BotaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExcluirActionPerformed
        // TODO add your handling code here:
        if (livroEmEdicao == null) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um livro na tabela para excluir.", "Nenhum Livro Selecionado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        int resposta = JOptionPane.showConfirmDialog(this,
                "Tem certeza que deseja excluir o livro '" + livroEmEdicao.getTitulo() + "'?",
                "Confirmar Exclusão",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

       
        if (resposta == JOptionPane.YES_OPTION) {
            // Remove o livro da nossa lista (o "banco de dados").
            listaLivros.remove(livroEmEdicao);
            JOptionPane.showMessageDialog(this, "Livro excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            
            limparCampos();
            habilitarCampos(false);
            carregarTabela(); 
        }
    }//GEN-LAST:event_BotaoExcluirActionPerformed

