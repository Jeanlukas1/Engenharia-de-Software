    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaLivros.getModel();
        modelo.setRowCount(0); // Limpa a tabela para evitar duplicatas

        
        for (Livro livro : listaLivros) {
            // Adiciona uma nova linha na tabela com os dados do livro
            modelo.addRow(new Object[]{
                livro.getId(),
                livro.getTitulo(),
                livro.getAutor(),
                livro.getAno()
            });
        }
    }

    /**
     * Habilita ou desabilita os campos do formulário e o botão Salvar.
     * @param habilitar true para habilitar, false para desabilitar.
     */
    private void habilitarCampos(boolean habilitar) {
        CampoTitulo.setEnabled(habilitar);
        CampoAutor.setEnabled(habilitar);
        CampoAno.setEnabled(habilitar);
        BotaoSalvar.setEnabled(habilitar);
    }

   
    private void limparCampos() {
        CampoTitulo.setText("");
        CampoAutor.setText("");
        CampoAno.setText("");
        this.livroEmEdicao = null; // Garante que não estamos mais em modo de edição
        tabelaLivros.clearSelection(); // Remove a seleção da tabela
    }
    
