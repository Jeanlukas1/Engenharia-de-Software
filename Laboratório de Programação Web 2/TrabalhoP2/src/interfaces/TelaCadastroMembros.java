    private void habilitarCampos(boolean habilitar) {
    campoNome.setEnabled(habilitar);
    campoEndereco.setEnabled(habilitar);
    campoTelefone.setEnabled(habilitar);
    botaoSalvar.setEnabled(habilitar);
}

    private void limparCampos() {
        campoNome.setText("");
        campoEndereco.setText("");
        campoTelefone.setText("");
        this.membroEmEdicao = null;
        tabelaMembros.clearSelection();
    }

    private void carregarTabela() {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tabelaMembros.getModel();
        modelo.setRowCount(0); // Limpa a tabela

        for (Membro membro : listaMembros) {
            modelo.addRow(new Object[]{
                membro.getId(),
                membro.getNome(),
                membro.getEndereco(),
                membro.getTelefone()
            });
        }
    }
    
