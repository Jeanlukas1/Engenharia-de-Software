    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        // TODO add your handling code here:
        String nome = campoNome.getText().trim();
        String endereco = campoEndereco.getText().trim();
        String telefone = campoTelefone.getText().trim();

        if (nome.isEmpty() || endereco.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha pelo menos Nome e Endereço.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (membroEmEdicao == null) { 
            Membro novoMembro = new Membro(nome, endereco, telefone);
            listaMembros.add(novoMembro);
        } else { 
            membroEmEdicao.setNome(nome);
            membroEmEdicao.setEndereco(endereco);
            membroEmEdicao.setTelefone(telefone);
        }

        limparCampos();
        habilitarCampos(false);
        carregarTabela();
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        // TODO add your handling code here:
        if (membroEmEdicao == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um membro para excluir.");
        return;
    }
    int resposta = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja excluir o membro " + membroEmEdicao.getNome() + "?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
    if (resposta == javax.swing.JOptionPane.YES_OPTION) {
        listaMembros.remove(membroEmEdicao);
        limparCampos();
        habilitarCampos(false);
        carregarTabela();
    }
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void tabelaMembrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMembrosMouseClicked
        // TODO add your handling code here:
        int linha = tabelaMembros.getSelectedRow();
        if (linha != -1) {
            int id = (int) tabelaMembros.getValueAt(linha, 0);
            for (Membro m : listaMembros) {
                if (m.getId() == id) {
                    membroEmEdicao = m;
                    break;
                }
            }
            if (membroEmEdicao != null) {
                campoNome.setText(membroEmEdicao.getNome());
                campoEndereco.setText(membroEmEdicao.getEndereco());
                campoTelefone.setText(membroEmEdicao.getTelefone());
                habilitarCampos(true);
            }
        }
    }//GEN-LAST:event_tabelaMembrosMouseClicked

